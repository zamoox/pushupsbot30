const CHALLENGE_TYPES = {
    pushups: { name: 'Віджимання', unit: 'разів' },
    squats:  { name: 'Присідання', unit: 'разів' },          // ✅ Виправлено назву
    abs:     { name: 'Прес / Скручування', unit: 'разів' }  // ✅ Виправлено назву
};

/**
 * ФУНКЦІЯ РАНДОМУ ДЛЯ КРОНУ: Обирає випадкову дисципліну на день для всього чату
 */
const getRandomDailyExercise = (lastExerciseType) => {
    const types = Object.keys(CHALLENGE_TYPES); // ['pushups', 'squats', 'abs']
    
    // Якщо вчорашнього типу немає або він чомусь бітий — беремо чистий рандом
    if (!lastExerciseType || !types.includes(lastExerciseType)) {
        const randomIndex = Math.floor(Math.random() * types.length);
        return types[randomIndex];
    }

    // Фільтруємо масив, залишаючи лише ті вправи, які НЕ збігаються з учорашньою
    const availableTypes = types.filter(type => type !== lastExerciseType); // Залишиться 2 варіанти
    
    const randomIndex = Math.floor(Math.random() * availableTypes.length);
    return availableTypes[randomIndex];
};

